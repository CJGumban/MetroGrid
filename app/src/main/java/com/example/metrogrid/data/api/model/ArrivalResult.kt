import com.google.gson.annotations.SerializedName

data class ArrivalResult(
	val arrivals: List<Arrival>,
	val realtimeDataUpdatedAt: Long
)

data class Arrival(
	val tripId: String,
	val stop: Stop,
	@SerializedName("when") val whenTime: String,
	val plannedWhen: String,
	val delay: Int,
	val platform: String? = null,
	val plannedPlatform: String? = null,
	val prognosisType: String,
	val direction: String? = null,
	val provenance: String,
	val line: Line,
	val remarks: List<Remark> = emptyList(),
	val origin: Stop,
	val destination: Stop? = null,
	val currentTripPosition: ArrivalLocation
)


data class ArrivalLocation(
	val type: String? = null,
	val id: String? = null,
	val latitude: Double,
	val longitude: Double
)





